//! Hershey "futural" (sans 1-stroke) font subset, ported from
//! `examples/python/hershey.py`. Used by the on-screen UI to render labels
//! and numeric values directly from Rust, no external client involvement.
//!
//! Coordinates are in the original font's space (~22 units tall, y-down).
//! `draw_text` flips Y and applies the requested baseline/size in NDC.

use crate::beam::BeamCommand;

/// Native cell height. All sizes passed to `draw_text` express character
/// height in NDC units; this is the divisor that turns size into a scale.
pub const CELL_HEIGHT: f32 = 22.0;

