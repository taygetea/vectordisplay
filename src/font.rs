/// Native width of `text` (uppercased internally) at the given size, in NDC.
pub fn text_width(text: &str, size: f32) -> f32 {
    let scale = size / CELL_HEIGHT;
    let mut total = 0.0f32;
    for ch in text.chars() {
        let (advance, _) = glyph(ch.to_ascii_uppercase());
        total += advance as f32;
    }
    total * scale
}

/// Emit beam commands to draw `text` so its baseline is at (x, y), each
/// glyph `size` NDC units tall. Returns the x cursor past the last glyph.
pub fn draw_text(
    out: &mut Vec<BeamCommand>,
    text: &str,
    x: f32,
    y: f32,
    size: f32,
    intensity: f32,
) -> f32 {
    let scale = size / CELL_HEIGHT;
    let mut cursor_x = x;
    for ch in text.chars() {
        let (advance, strokes) = glyph(ch.to_ascii_uppercase());
        for stroke in strokes {
            if let Some(&(fx, fy)) = stroke.first() {
                let px = cursor_x + fx as f32 * scale;
                let py = y + (CELL_HEIGHT - fy as f32) * scale;
                out.push(BeamCommand::MoveTo { x: px, y: py });
                for &(sx, sy) in &stroke[1..] {
                    let dx = cursor_x + sx as f32 * scale;
                    let dy = y + (CELL_HEIGHT - sy as f32) * scale;
                    out.push(BeamCommand::DrawTo {
                        x: dx,
                        y: dy,
                        intensity,
                    });
                }
            }
        }
        cursor_x += advance as f32 * scale;
    }
    cursor_x
}
